    private void inscription_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inscription_btnMouseClicked
        // TODO add your handling code here:
        try {

            String email = email_txt.getText();
            String logins = logins_txt.getText();
            String passwords = UtilsFonction.encrypt(new String(passwords_txt.getPassword()));
            String passwordsConfirme = new String(passwords_txt.getPassword());
            String passwordsConfirme1 = new String(confirm_passwords_txt.getPassword());
            String role = role_cbx.getSelectedItem().toString();


            if (!MyFonction.confirmPassword(passwordsConfirme, passwordsConfirme1)) {
                JOptionPane.showMessageDialog(null, "Les mots de passe ne sont pas identiques");
                return;
            }
            

            if (MyFonction.validerEmail(email)) {
                JOptionPane.showMessageDialog(null, "Veuillez entrer un email valide");
                return;
            }

            if (!MyFonction.validatePassword(passwordsConfirme)) {

                JOptionPane.showMessageDialog(null, "Veuillez entrer un mots de passe valide");
                return;
            }
            
            User userT = Utils.getiUser().getConnexion(logins);
             if(userT != null){
                 JOptionPane.showMessageDialog(null, "Ce login existe deja");
                return;
             
             }
             
              User userM = Utils.getiUser().getUserMail(email);
             if(userM != null){
                 JOptionPane.showMessageDialog(null, "Ce mail existe deja");
                return;
             
             }

            User user = new User(logins, passwords, email, role, "BLOQUER");
            User add = Utils.getiUser().addUser(user);
            if (add.getId() != null) {
                MyFonction.clearSignin(email_txt, logins_txt, passwords_txt, confirm_passwords_txt);
                JOptionPane.showMessageDialog(null, "votre compte a ete cree avec succes");
                signin_panel.setVisible(false);
                login_panel.setVisible(true);
            }
        } catch (RemoteException ex) {
            System.err.println("Erreur : " + ex.getMessage());
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_inscription_btnMouseClicked

